<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
				zhanghao: '',
				xingming: '',
				shouji: '',
				bisaimingcheng: '',
				tupian: '',
				baomingshijian: '',
				beizhu: '',
				qiuduixuanze: '',
				duizhangzhanghao: '',
				duizhangxingming: '',
				sfsh: '',
				shhf: '',
				userid: '',
				},
				qiuduixuanzeOptions: [],
				qiuduixuanzeIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   zhanghao : false,
                                   xingming : false,
                                   shouji : false,
                                   bisaimingcheng : false,
                                   tupian : false,
                                   baomingshijian : false,
                                   beizhu : false,
                                   qiuduixuanze : false,
                                   duizhangzhanghao : false,
                                   duizhangxingming : false,
                                   sfsh : false,
                                   shhf : false,
                                   userid : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			
			
			
			
			
			
			
			
			
			
			
			
			
		},
		async onLoad(options) {
        		this.ruleForm.baomingshijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.zhanghao = this.user.zhanghao
			this.ruleForm.xingming = this.user.xingming
			this.ruleForm.shouji = this.user.shouji

			// 下2
			res = await this.$api.option(`qiuduixinxi`,`qiuduimingcheng`,{});
			this.qiuduixuanzeOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`bisaibaoming`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='zhanghao'){
					this.ruleForm.zhanghao = obj[o];
					this.ro.zhanghao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='shouji'){
					this.ruleForm.shouji = obj[o];
					this.ro.shouji = true;
					continue;
					}
					if(o=='bisaimingcheng'){
					this.ruleForm.bisaimingcheng = obj[o];
					this.ro.bisaimingcheng = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='baomingshijian'){
					this.ruleForm.baomingshijian = obj[o];
					this.ro.baomingshijian = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
					if(o=='qiuduixuanze'){
					this.ruleForm.qiuduixuanze = obj[o];
					this.ro.qiuduixuanze = true;
					continue;
					}
					if(o=='duizhangzhanghao'){
					this.ruleForm.duizhangzhanghao = obj[o];
					this.ro.duizhangzhanghao = true;
					continue;
					}
					if(o=='duizhangxingming'){
					this.ruleForm.duizhangxingming = obj[o];
					this.ro.duizhangxingming = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async qiuduixuanzeChange (e) {
				this.qiuduixuanzeIndex = e.target.value
				this.ruleForm.qiuduixuanze = this.qiuduixuanzeOptions[this.qiuduixuanzeIndex]
				let res = await this.$api.follow(`qiuduixinxi`, `qiuduimingcheng`,{
					columnValue: this.ruleForm.qiuduixuanze
				});
				if(res.data.duizhangzhanghao){
					this.ruleForm.duizhangzhanghao = res.data.duizhangzhanghao
				}
				if(res.data.duizhangxingming){
					this.ruleForm.duizhangxingming = res.data.duizhangxingming
				}
			},

			// 多级联动参数


			// 日长控件选择日期时间
			baomingshijianConfirm(val) {
				console.log(val)
				this.ruleForm.baomingshijian = val.result;
				this.$forceUpdate();
			},


			tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if(this.ruleForm.id){
					await this.$api.update(`bisaibaoming`, this.ruleForm);
				}else{
					await this.$api.add(`bisaibaoming`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

